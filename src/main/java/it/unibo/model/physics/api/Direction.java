package it.unibo.model.physics.api;

/**
 * Models the concept of direction in 4 dimensions.
 */
public enum Direction {
    /**
     * The direction UP.
     */
    UP,
    /**
     * The direction DOWN.
     */
    DOWN,
    /**
     * The direction LEFT.
     */
    LEFT,
    /**
     * The direction RIGHT.
     */
    RIGHT;
}
